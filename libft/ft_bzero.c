#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned char *s1;

	s1 = (unsigned char*)s;
	while (n != 0)
	{
		*s1 = '\0';
		s1++;
		n--;
	}
	return (s);
}
